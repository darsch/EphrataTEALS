package sampleCode.Chapter2;

// This RedundantMultiplication class would print a multiplication table

public class RedundantMultiplication {

    public static void main(String[] args) {
        System.out.println(1 + " * " + 1 + " = " + (1 * 1));
        System.out.println(1 + " * " + 2 + " = " + (1 * 2));
        System.out.println(1 + " * " + 3 + " = " + (1 * 3));
        System.out.println(1 + " * " + 4 + " = " + (4 * 1));
        System.out.println(1 + " * " + 5 + " = " + (1 * 5));
        System.out.println(1 + " * " + 6 + " = " + (1 * 6));
        System.out.println(1 + " * " + 7 + " = " + (1 * 7));
        System.out.println(1 + " * " + 8 + " = " + (1 * 8));
        System.out.println(1 + " * " + 9 + " = " + (1 * 9));
        System.out.println(1 + " * " + 10 + " = " + (1 * 10));
    }
}
